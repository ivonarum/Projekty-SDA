
// Java Primitive Data Types
public class primitives {
    public static void main(String[] args) {
        String name = "John Doe"; // String is not technically primitive but commonly used > tworzy ciąg znaków
        int age = 30; // int > tworzy zmienną liczbową stałoprzecinkową (liczba całkowita)
        boolean isSubscribed = false; // boolean > tworzy zmienną logiczną, która przyjmuje wartość true or false
        // Java doesn't have a direct equivalent for null or undefined as a 'type', but null can be used as a value for non-primitive (Object) types
        
        System.out.println(name); // Displays the name > wyswietla na konsoli wartosc zmiennej "name" podaną w powyzszym kodzie
        System.out.println(age); // Displays the age > wyswietla na konsoli wartosc zmiennej "age" podaną w powyzszym kodzie
        System.out.println(isSubscribed); // Displays the boolean value > wyswietla na konsoli wartosc logiczną zmiennej "isSubscribed" podaną w powyzszym kodzie
    }
}
