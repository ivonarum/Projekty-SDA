
# Python Loops

# For loop
# zaczynamy od slowka for, kolejno dodajemy tzw iterator, potem slowo kluczowe 'in'
# range(x) okresla do kiedy pÄ™tla ma sie wykonywac
for i in range(5): # modyfikacja aby zaczal od innej liczby niz 0 > range(1,5)
    # wydrukowanie obecnej wartosci 'i' na konsole
    print(i)


# While loop
# utworzenie zmiennej
j = 0
while j < 5: # utworzenie warunku
    print(j)
    # j = j+1 czyli obecna wartosc j powiekszona o 1
    j += 1
