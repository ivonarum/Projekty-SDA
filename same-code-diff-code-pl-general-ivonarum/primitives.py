
# Python Primitive Data Types
name = "John Doe" # String > tworzy typ wartości tekstowych
age = 30 # Integer > tworzy wartosc liczbowa staloprzecinkowa
is_subscribed = False # Boolean > tworzy wartosc logiczną True lub False
height = None # NoneType > zmienna zainicjalizowana, ale nie ma przypisanej zadnej wartosci,  nie wskazuje na żaden obiekt w pamięci

print(name) # Displays the name > wyswietla na konsoli wartosc zmiennej "name" podaną w powyzszym kodzie
print(age) # Displays the age > wyswietla na konsoli wartosc zmiennej "age" podaną w powyzszym kodzie
print(is_subscribed) # Displays the boolean value  > wyswietla na konsoli wartosc logiczną zmiennej "isSubscribed" podaną w powyzszym kodzie
print(height) # Displays None
