
// JavaScript Primitive Data Types
let name = "John Doe"; // String > zmienna przypisuje wartość tekstową, ciąg znaków (nie liczby)
let age = 30; // Number > zmienna int , przypisuje liczbę całkowitą, stałoprzecinkową
let isSubscribed = false; // Boolean, zmienna przypisuje wartość logiczną False lub True
let height = null; // Null - nic, pustka, zmienna została zainicjalizowana, ale nie wskazuje na żaden obiekt w pamięci
let weight; // Undefined - "karton" weith jest pusty > zmienna została zadeklarowana, ale nie została zainicjalizowana ani przypisana żadna wartość

console.log(name); // Displays the name > wyswietla na konsoli wartosc zmiennej "name" podaną w powyzszym kodzie
console.log(age); // Displays the age > wyswietla na konsoli wartosc zmiennej "age" podaną w powyzszym kodzie
console.log(isSubscribed); // Displays the boolean value > wyswietla na konsoli wartosc logiczną zmiennej "isSubscribed" podaną w powyzszym kodzie
console.log(height); // Displays null > wyswietla na konsoli 'null'
console.log(weight); // Displays undefined > wyswietla na konsoli komunikat 'undefined'


