
// Java Variables
public class variables {
    public static void main(String[] args) {
        String message = "Hello, Java!"; // String > tworzenie tesktu
        final double PI = 3.14; // double > liczby zmiennoprzecinkowe
        boolean isAvailable = true; // boolean > wartosc logiczna

        System.out.println(message); // Displays the message > wyswietla na konsoli wartosc zmiennej "message" podaną w powyzszym kodzie
        System.out.println(PI); // Displays the PI constant > wyswietla na konsoli liczbę przypisaną do PI 
        System.out.println(isAvailable); // Displays the boolean value > wyswietla na konsoli wartosc logiczną podaną w powyzszym kodzie  
    

        // deklaracja zmiennej > utworzenie zmiennej - nowego 'pustego pudla', int -zmienna liczbowa staloprzecinkowa
        int wiek;
        // inicjalizacja zmiennej - przypisanie wartosci
        wiek = 28;
    }
}
