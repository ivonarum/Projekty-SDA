
# Python Variables
# String > tworzenie tekstu
message = "Hello, Python!" 
# zmienna double > liczba zmiennoprzecinkowa
PI = 3.14
# boolean > zmienna logiczna 
is_available = True

print(message) # Displays the message > wyjecie z 'pudla' "message" wartosci podanej w powyzszym kodzie > wyświetlenie na konsoli tej wartości
print(PI) # Displays the PI constant > wyświetla na konsoli wartosc PI
print(is_available) # Displays the boolean value > wyswietla na konsoli wartosc logiczną podaną w powyzszym kodzie
